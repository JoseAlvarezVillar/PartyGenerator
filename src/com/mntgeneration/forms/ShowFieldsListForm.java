    private DefaultListModel<String> getCurrentModel(JList list) {
        ListModel<String> model = list.getModel();
        DefaultListModel<String> dlm = new DefaultListModel<>();
        for(int i = 0; i < model.getSize(); i++ ) {
            dlm.addElement(model.getElementAt(i));
        }
        return dlm;
    }
    
    private void addElementsJList (JList list, Vector elements) {
        DefaultListModel dlm = getCurrentModel(list);
        ArrayList<String> elementsList = Collections.list(dlm.elements());
        elementsList.addAll(elements);
        //Let's sort the list and create new DefaultListModel which that list
        Collections.sort(elementsList); 
        DefaultListModel dlmTmp = new DefaultListModel();
        elementsList.forEach(dlmTmp::addElement);
        list.setModel(dlmTmp);
    }
    private void removeElementsJlist (JList list, Vector elements) {
        DefaultListModel dlm = getCurrentModel(list);
        elements.forEach(dlm::removeElement);    
        list.setModel(dlm);
    }
        
